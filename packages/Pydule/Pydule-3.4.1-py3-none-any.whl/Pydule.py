def shufflelist(l) -> list:
	if isinstance(l,list):
		import random as r

		ind,n=[],[]
		
		while True:
			ran=r.randint(0,len(l)-1)
			
			if len(ind)==len(l):
				break
			
			if ran not in ind:
				ind+=[ran]
		
		for i in ind:
			n+=[l[i]] 
		
		return n

	else:
		print(f'{err} Arguments Must {facemoji} Be in list {err}')

